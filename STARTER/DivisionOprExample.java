public class DivisionOprExample {
    public static void main(String[] args) {
        byte byte1 = 20;
        byte byte2 = 4;
        int byteQuotient = byte1 / byte2;
        System.out.println("byte / byte: " + byteQuotient);

        short short1 = 100;
        short short2 = 5;
        int shortQuotient = short1 / short2;
        System.out.println("short / short: " + shortQuotient);

        int int1 = 200;
        int int2 = 50;
        int intQuotient = int1 / int2;
        System.out.println("int / int: " + intQuotient);

        long long1 = 500L;
        long long2 = 25L;
        long longQuotient = long1 / long2;
        System.out.println("long / long: " + longQuotient);

        float float1 = 15.0f;
        float float2 = 3.0f;
        float floatQuotient = float1 / float2;
        System.out.println("float / float: " + floatQuotient);

        double double1 = 100.0;
        double double2 = 4.0;
        double doubleQuotient = double1 / double2;
        System.out.println("double / double: " + doubleQuotient);

        char char1 = 100;
        char char2 = 10;
        int charQuotient = char1 / char2;
        System.out.println("char / char: " + charQuotient);
    }
}

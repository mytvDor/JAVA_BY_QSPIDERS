public class MultiplicationOprExample {
    public static void main(String[] args) {
        byte byte1 = 5;
        byte byte2 = 4;
        int byteProduct = byte1 * byte2;
        System.out.println("byte * byte: " + byteProduct);

        short short1 = 15;
        short short2 = 20;
        int shortProduct = short1 * short2;
        System.out.println("short * short: " + shortProduct);

        int int1 = 10;
        int int2 = 30;
        int intProduct = int1 * int2;
        System.out.println("int * int: " + intProduct);

        long long1 = 25L;
        long long2 = 40L;
        long longProduct = long1 * long2;
        System.out.println("long * long: " + longProduct);

        float float1 = 1.2f;
        float float2 = 3.4f;
        float floatProduct = float1 * float2;
        System.out.println("float * float: " + floatProduct);

        double double1 = 12.3;
        double double2 = 4.5;
        double doubleProduct = double1 * double2;
        System.out.println("double * double: " + doubleProduct);

        char char1 = 3;
        char char2 = 2;
        int charProduct = char1 * char2;
        System.out.println("char * char: " + charProduct);
    }
}
